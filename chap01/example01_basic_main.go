// 示例 1：基础的 main 函数
// 这是最简单的 Go 程序结构

// 声明本代码所属的包
// package main 表示这是一个可执行程序
package main

// 导入 fmt 包，用于输入输出
import (
	"fmt"
)

// func main() 是程序的入口函数
// 注意：左大括号 { 必须与 func main() 在同一行
func main() {
	// 在屏幕上打印 "Hello, playground"
	fmt.Println("Hello, playground")
	// 注意：右大括号 } 必须单独占一行，并且与 func 对齐
}


