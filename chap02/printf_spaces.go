// 示例：fmt.Printf 中空格的处理
// 演示字符串中的空格和占位符补的空格都会被打印出来

package main

import "fmt"

func main() {
	// ============================================
	// 1. 字符串中直接写的空格会被打印
	// ============================================
	fmt.Println("=== 字符串中的空格 ===")
	
	// 字符串中直接写的空格会原样打印
	fmt.Printf("火星 体重：%.2f\n", 62.04)        // "火星" 和 "体重" 之间的空格会显示
	fmt.Printf("地球 年龄：%d 年\n", 41)          // "地球" 和 "年龄" 之间的空格会显示
	
	// 多个空格也会原样打印
	fmt.Printf("火星    体重：%.2f\n", 62.04)     // 多个空格都会显示
	
	fmt.Println()
	
	// ============================================
	// 2. 占位符补的空格也会被打印
	// ============================================
	fmt.Println("=== 占位符补的空格 ===")
	
	// %10f - 右对齐，不够前面补空格（这些空格会被打印）
	fmt.Printf("体重：[%10f]\n", 62.0412)        // 前面补的空格会显示
	fmt.Printf("体重：[%10.2f]\n", 62.04)        // 前面补的空格会显示
	
	// %-10f - 左对齐，不够后面补空格（这些空格也会被打印）
	fmt.Printf("体重：[%-10.2f]\n", 62.04)       // 后面补的空格会显示
	
	// 注意：虽然空格看不见，但它们确实存在，会影响对齐
	fmt.Println()
	
	// ============================================
	// 3. 空格都算在字符长度里
	// ============================================
	fmt.Println("=== 空格算在字符长度里 ===")
	
	// 演示：用方括号包围，可以看到空格的位置
	value := 62.04
	
	// 右对齐：前面补空格
	fmt.Printf("[%10.2f]\n", value)              // 总宽度10，数字占6位，前面补4个空格
	fmt.Printf("[%15.2f]\n", value)              // 总宽度15，数字占6位，前面补9个空格
	
	// 左对齐：后面补空格
	fmt.Printf("[%-10.2f]\n", value)             // 总宽度10，数字占6位，后面补4个空格
	fmt.Printf("[%-15.2f]\n", value)             // 总宽度15，数字占6位，后面补9个空格
	
	fmt.Println()
	
	// ============================================
	// 4. 实际应用：表格对齐（空格的作用）
	// ============================================
	fmt.Println("=== 表格对齐示例 ===")
	
	// 表头：使用左对齐，让文字左对齐
	fmt.Printf("%-10s %10s %10s\n", "姓名", "年龄", "体重")
	fmt.Println("----------------------------------------")
	
	// 数据行：姓名左对齐，数字右对齐
	fmt.Printf("%-10s %10d %10.2f\n", "张三", 25, 70.5)
	fmt.Printf("%-10s %10d %10.2f\n", "李四", 30, 75.8)
	fmt.Printf("%-10s %10d %10.2f\n", "王五", 28, 68.2)
	
	// 注意：这里的对齐效果就是靠占位符补的空格实现的
	// %-10s 让姓名左对齐（后面补空格）
	// %10d 和 %10.2f 让数字右对齐（前面补空格）
	
	fmt.Println()
	
	// ============================================
	// 5. 对比：有空格 vs 没有空格
	// ============================================
	fmt.Println("=== 对比：有空格 vs 没有空格 ===")
	
	// 没有控制宽度（没有补空格）
	fmt.Printf("体重：%.2f\n", 62.04)
	fmt.Printf("体重：%.2f\n", 123.45)
	fmt.Printf("体重：%.2f\n", 5.6)
	// 输出不对齐，因为数字长度不同
	
	fmt.Println()
	
	// 控制宽度（补空格对齐）
	fmt.Printf("体重：%10.2f\n", 62.04)   // 前面补空格，对齐
	fmt.Printf("体重：%10.2f\n", 123.45)  // 前面补空格，对齐
	fmt.Printf("体重：%10.2f\n", 5.6)     // 前面补空格，对齐
	// 输出对齐，因为占位符补了空格
	
	fmt.Println()
	
	// ============================================
	// 6. 字符串中的空格 + 占位符补的空格
	// ============================================
	fmt.Println("=== 组合：字符串空格 + 占位符空格 ===")
	
	// 字符串中的空格 + 占位符补的空格，都会显示
	fmt.Printf("火星 体重：[%10.2f] 公斤\n", 62.04)
	// "火星" 和 "体重" 之间的空格会显示
	// %10.2f 前面补的空格也会显示
	
	fmt.Printf("地球 年龄：[%10d] 年\n", 41)
	// "地球" 和 "年龄" 之间的空格会显示
	// %10d 前面补的空格也会显示
	
	fmt.Println()
	
	// ============================================
	// 7. 验证：空格确实被打印了
	// ============================================
	fmt.Println("=== 验证空格的存在 ===")
	
	// 用特殊字符标记空格的位置
	fmt.Println("下面每行都有10个字符（包括空格）：")
	fmt.Printf("'%10.2f'\n", 62.04)      // 用单引号包围，可以看到空格
	fmt.Printf("'%10.2f'\n", 123.45)     // 用单引号包围，可以看到空格
	fmt.Printf("'%10.2f'\n", 5.6)        // 用单引号包围，可以看到空格
	
	// 可以看到，数字前面确实有空格，这些空格都被打印出来了
}

