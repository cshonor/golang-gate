// 示例：import 导入包的两种方式
// 演示单个导入和多个导入的区别

package main

// 方式 1：导入单个包（不需要大括号）
// 直接写 import "包名" 即可
import "fmt"

// 注意：如果上面已经用了方式 1，就不能再用方式 2 了
// 一个文件只能有一个 import 语句块

func main() {
	fmt.Println("单个导入示例")
}

