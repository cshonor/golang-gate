// 示例：火星上的体重和年龄计算
// 第二章：美化的计算器
// 
// 为什么说火星能让人"变年轻、变苗条"？
// - 火星重力约为地球的 37.83%，所以体重会变轻（看起来更苗条）
// - 火星一年约 687 个地球日，所以年龄会变小（看起来更年轻）

// 声明本代码所属的包
package main

// 导入 fmt 包，用于打印输出
// 注意：如果只导入单个包，不需要大括号，直接写 import "fmt" 即可
// 如果要导入多个包，才需要用大括号：
// import (
//     "fmt"
//     "math"
// )
import "fmt"

// main 函数是程序的入口
func main() {
	// 计算在火星上的体重
	// 地球体重 164.0 磅 × 火星重力系数 0.3783 = 火星体重
	// 因为火星重力是地球的 37.83%，所以体重会变轻
	fmt.Println("你的重量在火星上:", 164.0*0.3783)

	// 计算在火星上的年龄
	// 地球年龄 41 年 × 365 天/年 ÷ 687 天/火星年 = 火星年龄
	// 因为火星一年有 687 个地球日，所以年龄会变小
	fmt.Println("你的年龄在火星上:", 41*365/687)
}


