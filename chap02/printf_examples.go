// 示例：fmt.Printf 格式化输出
// 演示各种占位符和格式化选项的使用

package main

import "fmt"

func main() {
	// ============================================
	// 1. 基本占位符
	// ============================================
	fmt.Println("=== 基本占位符 ===")
	
	// %s - 字符串
	name := "张三"
	fmt.Printf("名字：%s\n", name)
	
	// %d - 整数
	age := 25
	fmt.Printf("年龄：%d\n", age)
	
	// %f - 浮点数（默认保留6位小数）
	weight := 164.0
	fmt.Printf("体重：%f\n", weight)
	
	// %t - 布尔值
	isStudent := true
	fmt.Printf("是学生：%t\n", isStudent)
	
	// %v - 自动识别类型（通用占位符）
	fmt.Printf("名字：%v，年龄：%v，体重：%v\n", name, age, weight)
	
	fmt.Println()
	
	// ============================================
	// 2. 控制小数位数：%.nf
	// ============================================
	fmt.Println("=== 控制小数位数 ===")
	
	marsWeight := 164.0 * 0.3783
	fmt.Printf("保留1位小数：%.1f\n", marsWeight)
	fmt.Printf("保留2位小数：%.2f\n", marsWeight)
	fmt.Printf("保留4位小数：%.4f\n", marsWeight)
	
	fmt.Println()
	
	// ============================================
	// 3. 控制总宽度：%nf
	// ============================================
	fmt.Println("=== 控制总宽度 ===")
	
	// %4f - 总宽度4个字符，不够前面补空格
	fmt.Printf("宽度4：[%4f]\n", 1.23)      // 输出：[1.230000]（超过4位，原样输出）
	fmt.Printf("宽度4：[%4f]\n", 12.3)     // 输出：[12.300000]（超过4位，原样输出）
	fmt.Printf("宽度4：[%4d]\n", 5)        // 输出：[   5]（前面补3个空格）
	fmt.Printf("宽度4：[%4s]\n", "AB")     // 输出：[  AB]（前面补2个空格）
	
	fmt.Println()
	
	// ============================================
	// 4. 左对齐：%-nf
	// ============================================
	fmt.Println("=== 左对齐 ===")
	
	// %-15f - 总宽度15个字符，左对齐（不够后面补空格）
	fmt.Printf("左对齐15：[%-15f]\n", 62.0412)  // 数字后面补空格
	fmt.Printf("左对齐15：[%-15s]\n", "张三")    // 字符串后面补空格
	fmt.Printf("左对齐15：[%-15d]\n", 25)       // 数字后面补空格
	
	// 对比：右对齐（默认）
	fmt.Printf("右对齐15：[%15f]\n", 62.0412)   // 数字前面补空格
	fmt.Printf("右对齐15：[%15s]\n", "张三")     // 字符串前面补空格
	
	fmt.Println()
	
	// ============================================
	// 5. 组合使用：宽度 + 小数位数
	// ============================================
	fmt.Println("=== 组合使用：宽度 + 小数位数 ===")
	
	// %8.2f - 总宽度8个字符，保留2位小数
	fmt.Printf("格式8.2：[%8.2f]\n", 62.0412)   // 输出：[   62.04]
	fmt.Printf("格式8.2：[%8.2f]\n", 123.456)  // 输出：[  123.46]
	
	// %-8.2f - 左对齐，总宽度8个字符，保留2位小数
	fmt.Printf("左对齐8.2：[%-8.2f]\n", 62.0412)  // 输出：[62.04   ]
	fmt.Printf("左对齐8.2：[%-8.2f]\n", 123.456)  // 输出：[123.46  ]
	
	fmt.Println()
	
	// ============================================
	// 6. 多个占位符按顺序替换
	// ============================================
	fmt.Println("=== 多个占位符 ===")
	
	// 占位符和变量按顺序一一对应
	fmt.Printf("名字：%s，年龄：%d，体重：%.2f 公斤\n", "李四", 30, 75.5)
	
	// 火星计算示例
	earthWeight := 164.0
	marsWeight2 := earthWeight * 0.3783
	earthAge := 41
	marsAge := earthAge * 365 / 687
	
	fmt.Printf("地球体重：%.1f 磅，火星体重：%.2f 磅\n", earthWeight, marsWeight2)
	fmt.Printf("地球年龄：%d 年，火星年龄：%d 年\n", earthAge, marsAge)
	
	fmt.Println()
	
	// ============================================
	// 7. 实际应用：表格对齐
	// ============================================
	fmt.Println("=== 表格对齐示例 ===")
	
	fmt.Printf("%-10s %10s %10s\n", "姓名", "年龄", "体重")
	fmt.Println("----------------------------------------")
	fmt.Printf("%-10s %10d %10.2f\n", "张三", 25, 70.5)
	fmt.Printf("%-10s %10d %10.2f\n", "李四", 30, 75.8)
	fmt.Printf("%-10s %10d %10.2f\n", "王五", 28, 68.2)
}

